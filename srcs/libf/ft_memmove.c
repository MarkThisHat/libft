#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char		*d;
	const unsigned char	*s;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	if (s > d)
		ft_memcpy(d, s, n);
	else
		while (n--)
			d[n] = s[n];
	return (dest);
}
