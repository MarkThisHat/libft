#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*d;
	int		i;
	int		len;

	len = (int)ft_strlen(s) + 1;
	d = (char *)malloc(sizeof (char) * len);
	if (d == 0)
		return (NULL);
	i = 0;
	while (s[i])
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
